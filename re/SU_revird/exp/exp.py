def expand_key(key_bytes):
    rk = list(key_bytes)
    for i in range(1, 11):
        prev = rk[-4:]
        w_new = [STD_SBOX[prev[1]] ^ RCON[i], STD_SBOX[prev[2]], STD_SBOX[prev[3]], STD_SBOX[prev[0]]]
        for j in range(4): rk.append(rk[-16] ^ w_new[j])
        for _ in range(3):
            for j in range(4): rk.append(rk[-16] ^ rk[-4])
    return rk


