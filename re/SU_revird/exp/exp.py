def shift_rows(s):
    tmp = [0] * 16
    for row in range(4):
        for col in range(4):
            tmp[row + 4 * col] = s[row + 4 * ((col + row) % 4)]
    return tmp


def inv_shift_rows(s):
    tmp = [0] * 16
    for row in range(4):
        for col in range(4):
            tmp[row + 4 * col] = s[row + 4 * ((col - row + 4) % 4)]
    return tmp


def xtime(a):
    return ((a << 1) ^ 0x1b) & 0xFF if (a & 0x80) else (a << 1)


def mult(a, b):
    res = 0
    for _ in range(8):
        if b & 1: res ^= a
        a = xtime(a)
        b >>= 1
    return res


def mix_single_column(a):
    t = a[0] ^ a[1] ^ a[2] ^ a[3]
    return [
        a[0] ^ t ^ xtime(a[0] ^ a[1]),
        a[1] ^ t ^ xtime(a[1] ^ a[2]),
        a[2] ^ t ^ xtime(a[2] ^ a[3]),
        a[3] ^ t ^ xtime(a[3] ^ a[0])
    ]


def mix_columns(s):
    out = list(s)
    for i in range(4):
        c = mix_single_column([s[i * 4 + 0], s[i * 4 + 1], s[i * 4 + 2], s[i * 4 + 3]])
        out[i * 4:i * 4 + 4] = c
    return out


def inv_mix_single_column(a):
    return [
        mult(a[0], 14) ^ mult(a[1], 11) ^ mult(a[2], 13) ^ mult(a[3], 9),
        mult(a[0], 9) ^ mult(a[1], 14) ^ mult(a[2], 11) ^ mult(a[3], 13),
        mult(a[0], 13) ^ mult(a[1], 9) ^ mult(a[2], 14) ^ mult(a[3], 11),
        mult(a[0], 11) ^ mult(a[1], 13) ^ mult(a[2], 9) ^ mult(a[3], 14)
    ]


